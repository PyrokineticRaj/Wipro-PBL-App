package JavaFundamentals;

public class Flow_Control_Statements_Q4 {
    public static void main(String[] args) {
        char a = 'a';
        char b = 'b';
        if (a >= b) {
            System.out.println(a + "," + b);
        } else {
            System.out.println(b + "," + a);
        }
    }
}
